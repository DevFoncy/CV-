    public void Registrar_Familiar(){
        //Solo registra Padre y Madre 
        try {
            for(int i=0;i<2;i++){
            //Tabla FAMILIAR
            //----------------------------------------------
             fam1.setTIPO_FAM("Padre");
             if(i==1){
             fam1.setTIPO_FAM("Madre");   
            }
            fam1.setID_FAM(id_prueba);        
            fam1.setNOMBRE_FAM(tableFam.getValueAt(i,1).toString());
            fam1.setAÑO_NAC_FAM(Integer.parseInt(tableFam.getValueAt(i,2).toString()));
            fam1.setOCUP_FAM(tableFam.getValueAt(i,3).toString());
            
            regFam = "INSERT INTO FAMILIAR VALUES ("+fam1.getID_FAM()
                    +",'"+fam1.getTIPO_FAM()+"','"+fam1.getNOMBRE_FAM()+"',"+fam1.getAÑO_NAC_FAM()
                    +",'"+fam1.getOCUP_FAM()+"')";
                     
            conn.Escribir(regFam);
                                     
           }
                            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane,"Error: "+e.getMessage());
        }
    }
 
